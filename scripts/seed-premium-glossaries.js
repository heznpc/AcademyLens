function readJson(path) {
  return JSON.parse(readFileSync(join(ROOT, path), "utf8"));
}

function writeJson(path, value) {
  writeFileSync(join(ROOT, path), `${JSON.stringify(value, null, 2)}\n`);
}

function buildDraftGlossary(localeRecord, sourceGlossary) {
  const translations = targetTerms[localeRecord.locale];
  if (!translations) {
    throw new Error(`Missing target term map for ${localeRecord.locale}`);
  }

  const terms = sourceGlossary.terms.map((term) => {
    const target = translations[term.source];
    if (!target) {
      throw new Error(`Missing ${localeRecord.locale} translation for "${term.source}"`);
    }
    return {
      ...term,
      target,
      // Non-Korean drafts must not inherit the Korean source glossary's
      // per-term `note`: those rationales describe Korean-specific renderings
      // and previously leaked verbatim into every locale (es/de/fr/...),
      // contaminating each pack with Korean text. The standard draft
      // provenance note is locale-neutral; native reviewers add
      // locale-specific rationale when a pack is promoted past llm-drafted.
      note: DRAFT_NOTE
    };
  });

  return {
    schemaVersion: 1,
    locale: localeRecord.locale,
    name: localeRecord.name,
    status: localeRecord.status,
    sourceCatalog: sourceGlossary.sourceCatalog,
    protectedTerms: PROTECTED_TERMS,
    qaSignals: {
      draftMethod: "AI-assisted terminology draft",
      officialAlignment: "partial",
      googleTranslateBaseline: "pending",
      xTranslationCheck: "pending",
      communityReview: "open"
    },
    terms
  };
}

